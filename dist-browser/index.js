/**
 * Add two numbers
 * @param a Number one
 * @param b Number two
 * @return Result
 */
export var add = function (a, b) { return a + b; };
/**
 * sub two numbers
 * @param a Number one
 * @param b Number two
 * @return Result
 */
export var sub = function (a, b) { return a - b; };
/**
 * multiply two numbers
 * @param a Number one
 * @param b Number two
 * @return Result
 */
export var multiply = function (a, b) { return a * b; };
/**
 * divide two numbers
 * @param a Number one
 * @param b Number two
 * @return Result
 */
export var divide = function (a, b) { return a * b; };
